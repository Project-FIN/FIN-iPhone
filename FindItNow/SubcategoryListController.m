}

-(id) initWithStyle:(UITableViewStyle)style WithSubcategory:(NSArray*) subcate MapView:(MapViewController*) map
{
    self = [super initWithStyle:style];
    if (self){
        subcategories = subcate;
        mapView = map;
    }
    return self;
}

